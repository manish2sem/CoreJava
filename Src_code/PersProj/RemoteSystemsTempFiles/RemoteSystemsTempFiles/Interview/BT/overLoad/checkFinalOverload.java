package Interview.BT.overLoad;

/**
 * Compiler checks method signature for method overloading. 
 * 	Method signature consist of three things, 
		- Method Name   
		- Number Of Arguments   
		- Types of arguments.
	
	If these three things are same for any two methods in a class, then compiler gives duplicate method error.
	If not then methods are overloaded.

 * @author Manish
 *
 */

public class checkFinalOverload {
	
	public static void main(String... args) {
		checkFinalOverload checkFinal = checkFinalOverload();
		checkFinal.go(3);
	}
	
	void go(int x) {
		System.out.println("int "); 
	}
	
	void go(final int x) {
		System.out.println("int "); 
	}
	

}
