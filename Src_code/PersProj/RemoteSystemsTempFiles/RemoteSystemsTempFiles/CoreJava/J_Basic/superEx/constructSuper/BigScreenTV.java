package CoreJava.J_Basic.superEx.constructSuper;
/**
 * @author Manish
 * 
 * A call to super() or this() must be the first line of code in every constructor. 
 * If a constructor does not contain either, the compiler adds super() with empty parentheses 
 * as the first line of code in the constructor. 
 * 
 * In the BigScreenTV class, if the call to super() did not appear, 
 * the compiler would have added it for us. 
 *  
 */
public class BigScreenTV extends Television {
	
	public String aspectRatio;
	public short size;
	public BigScreenTV(){
		super();
		aspectRatio = "unknown";
		size = 40;
		System.out.println("Inside BigScreenTV()");
	}
	

}
