package DessignPattern.Other.Structrual.Adapter.A_Core.IF;
/*
 * Adapter(Wrapper) design pattern is a structural design pattern.
 * It converts the interface of one class into the interface that the client expect.
 * Adapter lets the classes work together that could not be possible because of incompatible interfaces.
 * 
 * Adapter is responsible for reusing existing code in new situations.
 * It translates one interface of a class into a compatible interface.
 * Adapter can make unrelated classes to work together in a single program. 
 */

public interface Shape {
	public void fill();
	public void display();
	public void unDisplay();

}




