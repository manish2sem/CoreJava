package DessignPattern.HeadFirst.Command.Problem.VendorClasses;

public class Stereo{
	
	public void on() {
		System.out.println("Stereo is on");
	}
	
	public void off() {
		System.out.println("Stereo is off");
	}
	
	public void setVolume() {
		System.out.println("Stereo Volume is set");
	}
	
	public void setRadio() {
		System.out.println("Radio in Stereo is set");
	}
	
	public void setDVD() {
		System.out.println("DVD in Stereo is set");
	}
	
	public void setCD() {
		System.out.println("CD in Stereo is set");
	}
}
