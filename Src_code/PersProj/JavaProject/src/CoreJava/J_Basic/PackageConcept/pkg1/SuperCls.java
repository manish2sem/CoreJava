package CoreJava.J_Basic.PackageConcept.pkg1;

public class SuperCls {
	
	public void superPublicMthd(){
		System.out.println("Inside Public method");
	}
	
	protected void superProtectedMthd(){
		System.out.println("Inside Protected method");
	}

	static void superStaticDefaultMthd(){
		System.out.println("Inside Protected method");
	}
	
	static public void superStaticPublicMthd(){
		System.out.println("Inside Protected method");
	}

}
