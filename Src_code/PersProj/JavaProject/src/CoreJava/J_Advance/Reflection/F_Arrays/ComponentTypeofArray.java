package CoreJava.J_Advance.Reflection.F_Arrays;


/**
 * The component type is the type of the items in the array.
 * 		- The component type of an int[] array is the int.class Class object. 
 * 		- The component type of a String[] array is the java.lang.String Class object.
 * 
 *	Obtaining the Component Type of an Array : Class.getComponentType()
 * 		- Get the Class object for an array 
 * 		- Access its component type via the Class.getComponentType() method.
 *
 */

public class ComponentTypeofArray {
	
	public static void main (String[] args) 
	{
		String[] strings = new String[3];
		Class stringArrayClass = strings.getClass();
		Class stringArrayComponentType = stringArrayClass.getComponentType();
		System.out.println(stringArrayComponentType);
	}

}
