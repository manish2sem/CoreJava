package CoreJava.J_Elementry.Operator;

/**
 * Bitwise operators compare two variables bit by bit, 
 * and return a variable whose bits have been set based on 
 * whether the two variables being compared had respective bits that were 
 * either both "on" (&),
 * one or the other "on" (|), or 
 * exactly one "on" (^)., or
 * complement/Negate (~)  
 *
 */

public class bitOpr {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		byte b1 = 6 & 8;
		byte b2 = 7 | 9;
		byte b3 = 5 ^ 4;
		System.out.println(b1 + " " + b2 + " " + b3);
	}

}
