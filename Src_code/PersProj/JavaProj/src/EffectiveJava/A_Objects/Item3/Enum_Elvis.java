/**
 * This approach is functionally equivalent to the public field approach, except that 
it is more concise, 
provides the serialization machinery for free, and 
provides an ironclad guarantee against multiple instantiation, 
even in the face of sophisticated serialization or reflection attacks.
 * single-element enum type is the best way to implement a singleton.
 * @author Manish
 *
 */
//Enum singleton - the preferred approach

public enum Enum_Elvis {
	INSTANCE;
	public void leaveTheBuilding() {  }
}
