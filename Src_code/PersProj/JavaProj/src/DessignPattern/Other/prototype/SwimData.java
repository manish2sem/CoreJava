package DessignPattern.Other.prototype;

public class SwimData implements Cloneable{
	public Object clone(){
		try{
			return super.clone();
		}catch(Exception e){
			System.out.println(e.getMessage());
			return null;
		}
	}
	
	public SwimData cloneMe(){
		try{
			return (SwimData)super.clone();
		}catch(Exception e){
			System.out.println(e.getMessage());
			return null;
		}
	}
}