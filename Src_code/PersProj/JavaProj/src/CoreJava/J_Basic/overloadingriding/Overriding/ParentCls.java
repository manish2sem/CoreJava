package CoreJava.J_Basic.overloadingriding.Overriding;

import java.io.FileNotFoundException;
import java.io.IOException;

public class ParentCls {
	
	protected void parentMethod() throws FileNotFoundException{
		System.out.println("Protected Method");
	}

}


class ChildCLs extends ParentCls {
	public void parentMethod() throws IOException{
		System.out.println("Protected Method");
	}

}