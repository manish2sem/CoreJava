package CoreJava.J_Elementry.WrapperClass.Integer;

/**
 * parseInt is an static method which, Parses the string argument as a signed decimal integer. 
 * The characters in the string must all be decimal digits, except that the first character may be an ASCII minus sign '-'.*
 * If the string does not contain a parsable integer NumberFormatException[RuntimeException] is thrown.
 * 
 */

public class A_ParseInt {
	public static void main(String[] args) {
        String strdata = "100";
        
        Integer intData = 100;
        Integer.parseInt(strdata);
        intData.parseInt(strdata);
        
    }


}
