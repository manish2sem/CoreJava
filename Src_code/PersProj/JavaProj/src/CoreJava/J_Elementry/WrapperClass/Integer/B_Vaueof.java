package CoreJava.J_Elementry.WrapperClass.Integer;

/**
 * Returns an Integer object holding the value of the specified String. 
 * 
 * This is exactly similar to Integer(parseInt(s));
 *
 *  If the string does not contain a parsable integer NumberFormatException[RuntimeException] is thrown.
 */

public class B_Vaueof {
	
	public static void main(String[] args) {
        String strdata = "100";
        
        Integer intData = 100;
        intData.valueOf(strdata);
        //This is exactly similar to Integer(parseInt(s));
        int i = Integer.valueOf("300");
        System.out.println(i);
        
    }

}
