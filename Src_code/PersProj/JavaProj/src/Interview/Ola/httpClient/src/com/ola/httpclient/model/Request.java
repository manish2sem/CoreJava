package Interview.Ola.httpClient.src.com.ola.httpclient.model;

import java.util.Map;

public class Request {
	
	int expectedRespCode;
	String url;
	String body;
	Map<String,String> headerParams;
		
	public int getExpectedRespCode() {
		return expectedRespCode;
	}
	public void setExpectedRespCode(int expectedRespCode) {
		this.expectedRespCode = expectedRespCode;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public Map<String, String> getHeaderParams() {
		return headerParams;
	}
	public void setHeaderParams(Map<String, String> headerParams) {
		this.headerParams = headerParams;
	}
	
}
